    private void btn_Toggle_UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_Toggle_UpdateActionPerformed
        // TODO add your handling code here:
        String updId = edt_Update_Id_Number.getText();
        if (updId.isEmpty()){
            JOptionPane.showMessageDialog(btn_Toggle_Update.getParent(),"Please Provide Required Details ","INFORMATION",JOptionPane.INFORMATION_MESSAGE);
            btn_Toggle_Update.setSelected(false);
        }
        else if (btn_Toggle_Update.isSelected()){
            String[] empData = new EmployeeDAO().searchEmployee(updId);
            if (empData[0] == null){
                JOptionPane.showMessageDialog(btn_Toggle_Update.getParent(),"Please Provide A Valid Id Number","WARNING",JOptionPane.WARNING_MESSAGE);
                edt_Update_Id_Number.setText("");
                btn_Toggle_Update.setSelected(false);
            }
            else{
                pnl_Update_Output.setVisible(true);
                btn_Update.setVisible(true);
                btn_Update_Clear.setVisible(true);
                lbl_Update_Id_Number.setEnabled(false);
                edt_Update_Id_Number.setEnabled(false);
                lbl_Update_Name_Output.setText(empData[1]);
                lbl_Update_Surname_Output.setText(empData[2]);
                lbl_Update_Username_Output.setText(empData[3]);

            }
        }
        else{
            pnl_Update_Output.setVisible(false);
            btn_Update.setVisible(false);
            btn_Update_Clear.setVisible(false);
            lbl_Update_Id_Number.setEnabled(true);
            edt_Update_Id_Number.setEnabled(true);
        }
    }//GEN-LAST:event_btn_Toggle_UpdateActionPerformed

