    private void btn_Remove_SupplierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_Remove_SupplierActionPerformed
        // TODO add your handling code here:
        tbl_Suppliers.setModel(new DefaultTableModel());
        String suppName = edt_Remove_Supplier_Name.getText();
        int suppId = Integer.parseInt(edt_Remove_Supplier_Id.getText());
        
       
        
        
        edt_Remove_Supplier_Name.setText("");
        edt_Remove_Supplier_Id.setText("");
        list_Remove_Suppliers.setModel(new DefaultListModel());
        lbl_Remove_Supplier_Name.setEnabled(false);
        edt_Remove_Supplier_Name.setEnabled(false);
        lbl_Remove_Supplier_Id.setEnabled(false);
        edt_Remove_Supplier_Id.setEnabled(false);
        btn_Remove_Supplier.setEnabled(false);
        
        
        int results = new SupplierDAO().removeSupplier(suppId, btn_Remove_Supplier.getParent());
        if (results == 0){
            JOptionPane.showMessageDialog(btn_Remove_Supplier.getParent(),"Incorrect Supplier Details","INCORRECT DETAILS",JOptionPane.INFORMATION_MESSAGE);
        }
        else if (results == 1){
            JOptionPane.showMessageDialog(btn_Remove_Supplier.getParent(),"Supplier Removed","SUCESSFUL REMOVAL",JOptionPane.INFORMATION_MESSAGE);
        }
        
        DefaultTableModel tblSupp = (DefaultTableModel)tbl_Suppliers.getModel();
        ArrayList<String> suppNames = new SupplierDAO().getSupplierNames();
        
        
        if (tblSupp.getRowCount() < suppNames.size()){
            tblSupp.addColumn("Supplier Name");
            for (int i = 0; i < suppNames.size(); i++){
                Object[] row = {suppNames.get(i)}; 
                tblSupp.addRow(row);
            }
            
        }
        
        
    }//GEN-LAST:event_btn_Remove_SupplierActionPerformed

