    private void btn_Save_SupplierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_Save_SupplierActionPerformed
        // TODO add your handling code here:
        
        String suppName = edt_New_Supplier_Name.getText();
        HashMap<String,Integer> srchResult = new SupplierDAO().searchSupplier(suppName);
        Set<String> keys = srchResult.keySet();
        
        if (suppName.isEmpty()){
            JOptionPane.showMessageDialog(btn_Save_Supplier.getParent(),"Please Provide Required Details ","INFORMATION",JOptionPane.INFORMATION_MESSAGE);
            edt_New_Supplier_Name.setText("");
        }
        
        else if(srchResult.isEmpty() == false){
            JOptionPane.showMessageDialog(btn_Save_Supplier.getParent(),"Trying to register existing supplier!","DUPLICATE SUPPLIER",JOptionPane.ERROR_MESSAGE);
            edt_New_Supplier_Name.setText("");
        }
        
        else{
            tbl_Suppliers.setModel(new DefaultTableModel());
            new SupplierDAO().registerSupplier(suppName);
            DefaultTableModel tblSupp = (DefaultTableModel) tbl_Suppliers.getModel();
            ArrayList<String> list = new SupplierDAO().getSupplierNames();
            if (tblSupp.getRowCount() < list.size()){
                tblSupp.addColumn("Supplier Name");
                for (int i = 0; i < list.size(); i++){
                    Object [] row = {list.get(i)};
                    tblSupp.addRow(row);
                }
            }
            edt_New_Supplier_Name.setText("");
        }    
    }//GEN-LAST:event_btn_Save_SupplierActionPerformed

