    private void btn_Remove_Search_SupplierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_Remove_Search_SupplierActionPerformed
        // TODO add your handling code here:
        list_Remove_Suppliers.setModel(new DefaultListModel());
        DefaultListModel suppList = (DefaultListModel)list_Remove_Suppliers.getModel();
        String sName;
        int sId = 0;
        String suppName = JOptionPane.showInputDialog(btn_Remove_Search_Supplier.getParent(),"Enter the name of the supplier: ","SEARCH SUPPLIER",JOptionPane.OK_CANCEL_OPTION);
        if (suppName != null){
            HashMap<String,Integer> rst = new SupplierDAO().searchSupplier(suppName);
            if (rst.isEmpty() == true){
                JOptionPane.showMessageDialog(btn_Remove_Search_Supplier.getParent(),"Please enter a valid supplier name ","INCORRECT INFORMATION",JOptionPane.ERROR_MESSAGE);
            }
            else if (rst.isEmpty() == false){
                suppList.addElement(("Supplier Names:"));
                suppList.addElement(" ");
                Set<Map.Entry<String,Integer>> rstSet = rst.entrySet();
                for (Map.Entry<String,Integer> n : rstSet){
                    sName = n.getKey();
                    sId = n.getValue();
                    suppList.addElement(sName);
                }
           }
        }
        else if (suppName == null){
            JOptionPane.showMessageDialog(btn_Remove_Search_Supplier.getParent(),"Please enter the required details","NO DETAILS SUBMITTED",JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btn_Remove_Search_SupplierActionPerformed

