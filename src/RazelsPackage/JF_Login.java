    private void btn_LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_LoginActionPerformed
        // TODO add your handling code here:
    
        String username,password;
        btn_Login.setEnabled(false);
        new JF_Login().setVisible(false);
        username = edt_Username.getText();
        password = edt_Password.getText();
        EmployeeDAO emp = new EmployeeDAO();
        Boolean bAuth = emp.authEmployee(password,username);
        //System.out.println(bAuth);
        //bAuth=true;
        
      
        if (username.isEmpty() || password.isEmpty() || username.isBlank() || password.isBlank()){
            JOptionPane.showMessageDialog(rootPane, "Please Enter Valid Login Credentials", "ERROR", HEIGHT);
            edt_Username.setText("");
            edt_Password.setText("");
            btn_Login.setEnabled(true);
        }
        
        else if(bAuth==false){
            JOptionPane.showMessageDialog(rootPane, "Please Enter Valid Login Credentials", "ERROR", HEIGHT);
            edt_Username.setText("");
            edt_Password.setText("");
            btn_Login.setEnabled(true);
        }
        
        else{
            btn_Login.setEnabled(false);
            new JF_Manager_Home().setVisible(true);
            new JF_Login().dispose();
        }
    }//GEN-LAST:event_btn_LoginActionPerformed

