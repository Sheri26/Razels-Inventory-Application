    private void edt_UsernameKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_edt_UsernameKeyPressed
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        if ((Character.isLetter(c)) || (Character.isISOControl(c)) || (Character.isWhitespace(c))  ){
            edt_Username.setEditable(true);
        }
        else{
            edt_Username.setEditable(false);
        }
    }//GEN-LAST:event_edt_UsernameKeyPressed

    private void edt_PasswordKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_edt_PasswordKeyPressed
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        if ((evt.getKeyChar() >= '0') && (evt.getKeyChar() <= '9')){
            edt_Password.setEditable(true);
        }
        else if ((evt.getExtendedKeyCode()== KeyEvent.VK_DELETE) || (evt.getExtendedKeyCode()== KeyEvent.VK_BACK_SPACE)){
            edt_Password.setEditable(true);
        }
        else {
            edt_Password.setEditable(false);
        }
    }//GEN-LAST:event_edt_PasswordKeyPressed

